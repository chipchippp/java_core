package DataType.DataType_5;

public class Main {
    public static void main(String[] args) {
        // 1. Declare a variable named 'number' of type int and assign it a value of 10
        int number = 10;

        // 2. Declare a variable named 'decimal' of type double and assign it a value of 10.5
        double decimal = 10.5;

        // 3. Declare a variable named 'character' of type char and assign it a value of 'A'
        char character = 'A';

        // 4. Declare a variable named 'isTrue' of type boolean and assign it a value of true
        boolean isTrue = true;

        // 5. Print the values of the variables
        System.out.println("number: " + number);
        System.out.println("decimal: " + decimal);
        System.out.println("character: " + character);
        System.out.println("isTrue: " + isTrue);
    }
}
